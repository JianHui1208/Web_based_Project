<h2 style="font-family: 'Seymour One';">The McDonald Brothers</h2>

<p style="font-size:20px;">Dick and Mac McDonald moved to California to seek opportunities they felt unavailable in New England. Failing in the movie business, they subsequently proved successful in operating drive-in restaurants. In 1948 they took a risk by streamlining their operations and introducing their Speedee Service System featuring 15 cent hamburgers. The restaurant’s success led the brothers to begin franchising their concept—nine becoming operating restaurants.</p>
