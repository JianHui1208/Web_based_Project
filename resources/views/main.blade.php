<h2 style="font-family: 'Seymour One';">The Story Behind the Little White Cup: AKA The Dunk Cup</h2>
<p style="font-size:20px;">McDonalds World Famous Fries and ketchup—what a combination! When Ray Kroc opened his first McDonald’s restaurant in 1955, our fries were meant to be eaten alone, sans ketchup. The tomato based condiment was reserved solely for hamburgers and cheeseburgers.

Yet, despite the practice McDonald’s customers frequently asked for ketchup for their fries. One even wrote the company inquiring as to the reason for this practice. The response he received included “…it is the policy of McDonald’s that catsup not be served with French fries”. (I know its ketchup!) As McDonald’s restaurants expanded across the U.S. so did requests for ketchup with fries.

Southern California McDonald’s franchisees Don and Enid Dunkleman heeded their customers’ requests by creating “The Dunk Cup”. Their inspiration consisted of a modified small drink cup complete with a plastic drink lid. Each Dunk Cup containing ketchup was prepared in the morning and was placed in the refrigerator until a customer requested ketchup. Initially the Dunk Cup cost three cents or a nickel but shortly afterwards they were provided complimentary. To accommodate carry out orders ketchup packets were introduced in 1968. During the intervening years, the ubiquitous little white serving cup became a fixture in the restaurant for customers dining in.</p>
