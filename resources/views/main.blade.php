<h1 style="font-family: 'Seymour One';">Ray Kroc</h1>

<p style="font-size:20px;"> A native Chicagoan, Ray Kroc left high school after his sophomore year to join the World War One Red Cross Ambulance Corps. The war ended before his unit was sent overseas with Ray returning home to earn a living as a musician and later selling paper cups. In 1939, he became the exclusive distributor of the Multimixer (a milkshake mixing machine). He visited the McDonald brothers in 1954 which led to him becoming their franchise agent. He opened up the first restaurant for McDonald’s System, Inc., a predecessor of McDonald’s Corp. in Des Plaines, Illinois in April, 1955. McDonald’s acquired the rights to the brother’s company in 1961 for $2.7 million.</p>

